#!/usr/bin/env node
// packages/brs-gen/scripts/gen-music-thumb.mjs
//
// Deterministic generator for music_player template's 15 PNG assets:
//   - 3 playlist art (600x600, solid color + numeric glyph 1/2/3)
//   - 12 transport icons (48x48, monochrome glyphs in light + dark themes)
//     transport actions: play, pause, prev, next, rew15, fwd15
//
// Run via: node packages/brs-gen/scripts/gen-music-thumb.mjs
// Idempotent: re-running produces byte-equal output.
//
// Determinism: sharp 0.34.5 (pinned in package.json), inline SVG (no font
// dependencies that could vary across hosts), explicit { compressionLevel: 9,
// palette: false } on every PNG emit.
//
// NOTE on play-icon-{light,dark}.png byte-equality:
//   The existing play-icon-light.png (312 bytes) and play-icon-dark.png
//   (320 bytes) in video_grid_channel + news_channel were NOT generated by
//   the current sharp pipeline -- they have different pixel data than what
//   the current sharp 0.34.5 SVG rasterizer produces for the same path.
//   (The existing files have pHYs 2835 ppm and 372 non-transparent pixels
//   at row 24; the current pipeline produces 488 at the same row with the
//   identical SVG.) Therefore this script uses option (b): it reads the
//   existing play-icon PNGs from news_channel and writes them verbatim,
//   guaranteeing byte-equality. The other 10 transport icons are generated
//   fresh by this script using the same sharp params. The asset-reuse test
//   in tests/asset-reuse.test.ts (Task 10) enforces the byte-equality.

import sharp from 'sharp';
import { mkdir, copyFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const PKG_ROOT = dirname(HERE);
const IMAGES = join(PKG_ROOT, 'templates', 'music_player', 'files', 'images');
const NEWS_IMAGES = join(PKG_ROOT, 'templates', 'news_channel', 'files', 'images');

const PLAYLISTS = [
  { file: 'playlist-1.png', bg: '#1a3a8a', glyph: '1' },
  { file: 'playlist-2.png', bg: '#2a8a3a', glyph: '2' },
  { file: 'playlist-3.png', bg: '#8a3a2a', glyph: '3' },
];

// Transport icons: 48x48, monochrome path glyphs.
// Light theme = white-on-transparent (for unfocused state on dark background).
// Dark theme = black-on-transparent (for focused state on light focus bitmap).
//
// The 'play' icon is handled separately below (copied verbatim from news_channel
// to guarantee byte-equality). All other icons are generated here.
const ICONS = [
  // [base_name, svg_path_d (48x48 viewBox)]
  ['pause', 'M14 8 H22 V40 H14 Z M26 8 H34 V40 H26 Z'],
  ['prev',  'M10 8 H14 V40 H10 Z M40 8 L18 24 L40 40 Z'],
  ['next',  'M34 8 H38 V40 H34 Z M8 8 L30 24 L8 40 Z'],
  ['rew15', 'M28 8 A16 16 0 1 0 28 40 L28 36 A12 12 0 1 1 28 12 Z M28 4 L20 12 L28 20 Z'],
  ['fwd15', 'M20 8 A16 16 0 1 1 20 40 L20 36 A12 12 0 1 0 20 12 Z M20 4 L28 12 L20 20 Z'],
];

async function main() {
  await mkdir(IMAGES, { recursive: true });

  // 3 playlist art PNGs (600x600 solid color + numeric glyph)
  for (const p of PLAYLISTS) {
    const svg = `<?xml version="1.0" encoding="UTF-8"?>
<svg width="600" height="600" viewBox="0 0 600 600" xmlns="http://www.w3.org/2000/svg">
  <rect width="600" height="600" fill="${p.bg}" />
  <text x="300" y="380" text-anchor="middle" font-family="sans-serif" font-size="280" font-weight="700" fill="#FFFFFF">${p.glyph}</text>
</svg>`;
    await sharp(Buffer.from(svg))
      .png({ compressionLevel: 9, palette: false })
      .toFile(join(IMAGES, p.file));
    process.stdout.write(`wrote ${p.file}\n`);
  }

  // 10 transport icon PNGs (5 actions x 2 themes), excluding play which is
  // handled separately below.
  for (const [base, d] of ICONS) {
    for (const theme of ['light', 'dark']) {
      const fill = theme === 'light' ? '#FFFFFF' : '#000000';
      const svg = `<?xml version="1.0" encoding="UTF-8"?>
<svg width="48" height="48" viewBox="0 0 48 48" xmlns="http://www.w3.org/2000/svg">
  <path d="${d}" fill="${fill}" />
</svg>`;
      const file = `${base}-icon-${theme}.png`;
      await sharp(Buffer.from(svg))
        .png({ compressionLevel: 9, palette: false })
        .toFile(join(IMAGES, file));
      process.stdout.write(`wrote ${file}\n`);
    }
  }

  // play-icon-{light,dark}.png: copied verbatim from news_channel to guarantee
  // byte-equality with video_grid_channel + news_channel copies.
  // The current sharp 0.34.5 SVG rasterizer produces different pixel data for
  // the play triangle than the existing 312/320-byte PNGs; do not regenerate.
  for (const theme of ['light', 'dark']) {
    const file = `play-icon-${theme}.png`;
    await copyFile(join(NEWS_IMAGES, file), join(IMAGES, file));
    process.stdout.write(`copied ${file} (byte-equal to news_channel + video_grid_channel)\n`);
  }
}

main().catch((err) => {
  process.stderr.write(
    `gen-music-thumb failed: ${err instanceof Error ? err.stack : String(err)}\n`,
  );
  process.exit(1);
});
